package com.cosog.model;

public class ItemDataGroupTwo {
	private String dataitem1;
	private Float value1;
	private String dataitem2;
	private Float value2;
	public ItemDataGroupTwo() {
		super();
	}
	public ItemDataGroupTwo(String dataitem1, Float value1, String dataitem2, Float value2) {
		super();
		this.dataitem1 = dataitem1;
		this.value1 = value1;
		this.dataitem2 = dataitem2;
		this.value2 = value2;
	}
	public String getDataitem1() {
		return dataitem1;
	}
	public void setDataitem1(String dataitem1) {
		this.dataitem1 = dataitem1;
	}
	public Float getValue1() {
		return value1;
	}
	public void setValue1(Float value1) {
		this.value1 = value1;
	}
	public String getDataitem2() {
		return dataitem2;
	}
	public void setDataitem2(String dataitem2) {
		this.dataitem2 = dataitem2;
	}
	public Float getValue2() {
		return value2;
	}
	public void setValue2(Float value2) {
		this.value2 = value2;
	}
	
}
